#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*new;

	new = (char *) malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!new)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
